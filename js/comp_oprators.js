// Comparison Operators in JavaScript

let a = 5;
let b = '5';
let c = 10;

console.log("a == b :", a == b);    // true (loose equality, type conversion)
console.log("a === b :", a === b);  // false (strict equality, no type conversion)
console.log("a != c :", a != c);    // true (loose not equal)
console.log("a !== b :", a !== b);  // true (strict not equal)
console.log("c > a :", c > a);      // true (greater than)
console.log("a >= 5 :", a >= 5);    // true (greater than or equal)
console.log("a < c :", a < c);      // true (less than)
console.log("a <= 5 :", a <= 5);    // true (less than or equal)
